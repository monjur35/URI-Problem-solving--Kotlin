    }
    else if (ddd==11){
        println("Sao Paulo")
    }
    else if (ddd==21){
        println("Rio de Janeiro")
    }
    else if (ddd==32){
        println("Juiz de Fora")
    }
    else if (ddd==19){
        println("Campinas")
    }
    else if (ddd==27){
